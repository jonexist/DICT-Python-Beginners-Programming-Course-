# Strings
name = "Jonathan"
lastname = "Goboli"

print(name, lastname)

# Numbers and Float
age = 21
my_money = 500.50

print(age, my_money)

# Boolean true/false value
is_high = 50
is_less = 40

if is_high > is_less:
    print("50 is greater than 40")
else:
    print("40 is less than 50")

# Converting float to a number or whole number using int() function
print(int(73.605))
# Converting whole number to a float using float() function
print(float(73))
# Converting whole number to a string using str() function
print(str(500))
# Print every individual character to our string using tuple() function
print(tuple("This is a string."))
# Converting string into a list using list() function
print(list("Jonathan"))
