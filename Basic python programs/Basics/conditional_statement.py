# if keyword statement
num1 = 10

if num1 >= 0:
  print("It's a positive number.")

# else keyword statement
x = 10 

if x > 45:
  print("Yes")
else:
  print("No")

# elif keyword statement
food_cost = 400
if food_cost > 400:
  print("I will not buy food")
elif food_cost == 400:
  print("I will buy spaghetti.")
elif food_cost < 400:
  print("I will buy spaghetti and Iced tea.")