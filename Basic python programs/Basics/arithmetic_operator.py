# Arithmetic Operators & Precedence

# Addition
print(10 + 10)
# Subtraction
print(10 - 100)
# Multiplication
print(10 * 10)
# Division
print(10 / 100)

# Modulus Operator
print(741 % 33)

# Exponentiation operator
print(7 ** 3)

# Floor division operator
print(36 // 8)

# Precedence of these operators
print((4-3) * 6 ** 2)
