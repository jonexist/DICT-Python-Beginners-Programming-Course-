# Problem
x = 200 
y = 110
# equal to operator determines if the values are equal
print(x == y)
# not equal to operator determines if the values are not equal
print(x != y)
# greater than operator determines if the first value is greater than the other value
print(x > 11)
# less than operator determines if the first value is less than the other value
print(x < y)
# greater than or equal to operator determines if the first value is greater than or equal to the other value
print(x >= y)
# less than or equal to operator determines if the first value is less than or equal to the other value
print(x <= y)